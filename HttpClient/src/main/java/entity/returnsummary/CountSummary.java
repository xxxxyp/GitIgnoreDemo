package entity.returnsummary;

public class CountSummary {

    private int count;
    private String type;
    public void setCount(int count) {
         this.count = count;
     }
     public int getCount() {
         return count;
     }

    public void setType(String type) {
         this.type = type;
     }
     public String getType() {
         return type;
     }

}